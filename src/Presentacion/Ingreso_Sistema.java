    private void btnaceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnaceptarActionPerformed
        if(this.txt_user.getText().isEmpty() || this.txt_pass.getPassword().length == 0){
            JOptionPane.showMessageDialog(this, "Debe ingresar Usuario y Contrase単a");
            return;
        }
        
        String sql = "select * from usuario where user=? and pass=?";
        
        try{
            pst = conn.prepareStatement(sql);
            pst.setString(1,txt_user.getText());
            pst.setString(2,txt_pass.getText());
            rs = pst.executeQuery();
            
            if(rs.next()){
                JOptionPane.showMessageDialog(null,"El usuario y contrase単a son correctos");
                this.dispose();
                //close();
                
                Interfaz_Principal ip = new Interfaz_Principal();
                ip.setVisible(true);
            }   
            else{
                JOptionPane.showMessageDialog(null,"El usuario y contrase単a son incorrectos");
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }
    }//GEN-LAST:event_btnaceptarActionPerformed

